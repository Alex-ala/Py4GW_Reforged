# live path-planning test (Data tab): plan player -> a chosen known exit,
# with 800u no-go zones on every OTHER portal, and optionally draw it.
_path_test = {"running": False, "result": None, "error": None, "draw": False}


def _path_test_coro(target_exit):
    try:
        map_id = Map.GetMapID()
        ap = AutoPathing()
        yield from ap.load_pathing_maps()
        navmesh = ap.get_navmesh()
        if navmesh is None:
            _path_test["error"] = "navmesh not available"
            return
        player_id = Player.GetAgentID()
        px, py, _pz = Agent.GetXYZ(player_id)
        zones = pathing_ext.zones_for_map(
            _active_portal_graph(), map_id, map_completed=True,
            target_neighbor=target_exit.neighbor_id,
        )
        yield
        path = pathing_ext.plan_path_avoiding(
            navmesh, AStar, (px, py),
            (target_exit.portal_x, target_exit.portal_y), zones,
        )
        if path is None:
            _path_test["error"] = "no route found (zones too tight?)"
        else:
            _path_test["result"] = {
                "target": f"{target_exit.neighbor_name} ({target_exit.neighbor_id})",
                "waypoints": path,
                "length": pathing_ext.path_length(path),
                "zones": zones.circles,
                "target_portal": (target_exit.portal_x, target_exit.portal_y),
            }
    except Exception:
        _path_test["error"] = traceback.format_exc()
    finally:
        _path_test["running"] = False


def _draw_polyline_3d(overlay, points, color, thickness=3.0):
    for (x1, y1), (x2, y2) in zip(points, points[1:]):
        if not GLOBAL_CACHE.Camera.IsPointInFOV(x1, y1) \
                or not GLOBAL_CACHE.Camera.IsPointInFOV(x2, y2):
            continue
        z1 = overlay.FindZ(x1, y1)
        z2 = overlay.FindZ(x2, y2)
        overlay.DrawLine3D(x1, y1, z1, x2, y2, z2, color, thickness)


def _draw_zones_3d(overlay, circles):
    for cx, cy, r in circles:
        if not GLOBAL_CACHE.Camera.IsPointInFOV(cx, cy):
            continue
        z = overlay.FindZ(cx, cy)
        overlay.DrawPoly3D(cx, cy, z, r,
                           Color(255, 60, 60, 200).to_color(), 48, 4.0, False)


def _draw_portal_arrow_3d(overlay, x, y, color, label=None,
                          shaft_height=300.0, head_size=90.0):
    """A location marker at (x, y): a vertical pole plus 4 lines
    converging from a ring at the pole's top down to the ground point,
    forming a downward-pointing "funnel" silhouette. No true portal-facing
    direction is available from the graph data (only a position), so this
    marks WHERE a portal is rather than which way it opens -- a ring of
    converging lines reads as an arrow from any horizontal camera angle,
    unlike a single flat triangle which can go edge-on and vanish."""
    if not GLOBAL_CACHE.Camera.IsPointInFOV(x, y):
        return
    z = overlay.FindZ(x, y)
    top = z + shaft_height
    ring_z = top - head_size
    overlay.DrawLine3D(x, y, top, x, y, ring_z, color, 3.0)
    for ang in (0.0, math.pi / 2, math.pi, 3 * math.pi / 2):
        ox = x + head_size * 0.5 * math.cos(ang)
        oy = y + head_size * 0.5 * math.sin(ang)
        overlay.DrawLine3D(ox, oy, ring_z, x, y, z, color, 3.0)
    if label:
        overlay.DrawText3D(x, y, top, label, color, True, True, 1.0)


def _draw_all_portals_overlay():
    """Diagnostic (user request): every portal on the CURRENT map -- both
    known graph edges (arrow + destination label) and offline/live
    portal-marker props with an unresolved destination (zone only, no
    label -- we don't know where they lead) -- plus the exact no-go zone
    circles zones_for_map() would compute for coverage/vanquish (radius
    PORTAL_ZONE_RADIUS, map_completed=False so every portal is included,
    same as what a coverage walk actually treats as blocked). Recomputed
    fresh every frame off the live current map, not tied to a test button."""
    if not _portal_overlay["show"]:
        return
    try:
        map_id = Map.GetMapID()
    except Exception:
        return
    graph = _active_portal_graph()
    edges = graph.adjacency().get(map_id, [])
    zones = pathing_ext.zones_for_map(graph, map_id, map_completed=False)
    overlay = Overlay()
    overlay.BeginDraw()
    try:
        _draw_zones_3d(overlay, zones.circles)
        arrow_color = Color(0, 220, 255, 255).to_color()
        for e in edges:
            _draw_portal_arrow_3d(overlay, e.portal_x, e.portal_y,
                                  arrow_color, label=e.neighbor_name)
        marker_color = Color(255, 220, 0, 255).to_color()
        known_pos = {(round(e.portal_x), round(e.portal_y)) for e in edges}
        for mx, my in graph.portal_markers(map_id):
            if (round(mx), round(my)) in known_pos:
                continue  # already drawn (with a label) as a known edge
            _draw_portal_arrow_3d(overlay, mx, my, marker_color)
    finally:
        overlay.EndDraw()


def _draw_run_waypoints_overlay():
    """Diagnostic: draw the upcoming coverage waypoints of the ACTUAL run
    (RunContext.current_waypoints/current_waypoint_index, updated live by
    runner.run_single_map) -- separate from the manual "Plan path"/"Plan
    coverage walk" test buttons, this shows what the running bot is
    walking right now, so a stuck/confused run can be diagnosed visually."""
    if not _wp_draw["show"] or _run_ctx is None:
        return
    wps = _run_ctx.current_waypoints
    if not wps:
        return
    idx = _run_ctx.current_waypoint_index
    upcoming = wps[idx:idx + _wp_draw["count"]]
    if len(upcoming) < 2:
        return
    overlay = Overlay()
    overlay.BeginDraw()
    try:
        _draw_polyline_3d(overlay, upcoming, Color(255, 165, 0, 255).to_color(), 3.0)
    finally:
        overlay.EndDraw()


def _draw_path_test_overlay():
    result = _path_test["result"]
    if not result or not _path_test["draw"]:
        return
    overlay = Overlay()
    overlay.BeginDraw()
    try:
        _draw_polyline_3d(overlay, result["waypoints"],
                          Color(0, 255, 0, 255).to_color())
        # exclusion zones in red; the exempted target portal as a green ring
        _draw_zones_3d(overlay, result["zones"])
        tx, ty = result["target_portal"]
        if GLOBAL_CACHE.Camera.IsPointInFOV(tx, ty):
            z = overlay.FindZ(tx, ty)
            overlay.DrawPoly3D(tx, ty, z, pathing_ext.PORTAL_ZONE_RADIUS,
                               Color(60, 255, 60, 200).to_color(), 48, 4.0, False)
    finally:
        overlay.EndDraw()


