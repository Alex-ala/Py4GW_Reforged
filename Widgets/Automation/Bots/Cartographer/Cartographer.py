# live coverage-walk test (Data tab): generate the full-map coverage
# waypoint order with ALL portals zoned (map_completed=False) and draw it.
_coverage_test = {"running": False, "result": None, "error": None, "draw": False}


def _coverage_test_coro():
    try:
        map_id = Map.GetMapID()
        ap = AutoPathing()
        yield from ap.load_pathing_maps()
        navmesh = ap.get_navmesh()
        if navmesh is None:
            _coverage_test["error"] = "navmesh not available"
            return
        player_id = Player.GetAgentID()
        px, py, _pz = Agent.GetXYZ(player_id)
        zones = pathing_ext.zones_for_map(_active_portal_graph(), map_id,
                                          map_completed=False)
        try:
            live_portals = Map.Pathing.GetTravelPortals() or []
        except Exception:
            live_portals = []
        if live_portals:
            zones.circles.extend(
                (p.x, p.y, pathing_ext.PORTAL_ZONE_RADIUS) for p in live_portals)
        yield
        wps = coverage.generate_coverage_waypoints(navmesh, zones, (px, py))
        if not wps:
            _coverage_test["error"] = "no coverage waypoints generated"
        else:
            _coverage_test["result"] = {
                "waypoints": wps,
                "length": pathing_ext.path_length(wps),
                "zones": zones.circles,
            }
    except Exception:
        _coverage_test["error"] = traceback.format_exc()
    finally:
        _coverage_test["running"] = False


def _draw_coverage_test_overlay():
    result = _coverage_test["result"]
    if not result or not _coverage_test["draw"]:
        return
    overlay = Overlay()
    overlay.BeginDraw()
    try:
        _draw_polyline_3d(overlay, result["waypoints"],
                          Color(80, 160, 255, 255).to_color(), 2.0)
        _draw_zones_3d(overlay, result["zones"])
    finally:
        overlay.EndDraw()


