def main():
    # Every begin_tab_item()/begin_tab_bar() below is matched via
    # try/finally, and the whole draw is wrapped in try/except, so a bug
    # in any tab's draw function can never skip our own end_tab_item()/
    # end_tab_bar()/end() calls -- an unbalanced Begin/End or
    # PushStyle/PopStyle here doesn't just break our own window, it
    # corrupts ImGui's stack for whatever the harness draws next this
    # frame (seen previously as "Missing End()"/"Missing PopStyleColor()"
    # errors on unrelated windows like the LaunchBar and Debug console).
    if PyImGui.begin(MODULE_NAME):
        try:
            if _load_error is not None:
                PyImGui.text_colored("Data load FAILED:", (1.0, 0.3, 0.3, 1.0))
                PyImGui.text(_load_error)
            else:
                if PyImGui.begin_tab_bar("##CartographerTabs"):
                    try:
                        if PyImGui.begin_tab_item("Main"):
                            try:
                                _draw_main_tab()
                            finally:
                                PyImGui.end_tab_item()
                        if PyImGui.begin_tab_item("Loot"):
                            try:
                                _draw_loot_tab()
                            finally:
                                PyImGui.end_tab_item()
                        if PyImGui.begin_tab_item("Stores"):
                            try:
                                _draw_stores_tab()
                            finally:
                                PyImGui.end_tab_item()
                        if PyImGui.begin_tab_item("Data"):
                            try:
                                _draw_data_tab()
                            finally:
                                PyImGui.end_tab_item()
                    finally:
                        PyImGui.end_tab_bar()
                settings.tick()
        except Exception:
            PyImGui.text_colored("Cartographer draw error (see log):",
                                 (1.0, 0.3, 0.3, 1.0))
            PyImGui.text(traceback.format_exc().splitlines()[-1])
    PyImGui.end()
    if _load_error is None:
        try:
            _draw_path_test_overlay()
            _draw_coverage_test_overlay()
            _draw_run_waypoints_overlay()
            _draw_all_portals_overlay()
        except Exception:
            pass


if __name__ == "__main__":
    main()
