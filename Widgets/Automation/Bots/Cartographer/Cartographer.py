def _active_portal_graph():
    """Which portal-graph module to use for routing/pathing this frame:
    our own discovery pipeline, or GWToolboxpp's static hand-curated list
    (Settings.use_gwtoolbox_routing). Both expose the identical API
    (load/adjacency/neighbors/portal_position/portal_markers/known_map_ids)
    so every caller can just swap the module reference."""
    return gwtoolbox_portal_graph if settings.use_gwtoolbox_routing else portal_graph


def _current_target_model_id() -> int:
    """Model id of the current target: item model for a dropped item,
    agent model for anything living. 0 if no/unknown target."""
    try:
        tid = Player.GetTargetID()
        if tid <= 0:
            return 0
        item_agent = Agent.GetItemAgentByID(tid)
        if item_agent is not None and item_agent.item_id:
            return int(Item.GetModelID(item_agent.item_id) or 0)
        return int(Agent.GetModelID(tid) or 0)
    except Exception:
        return 0


def _track_target_model_id():
    """Keep the healer/loot model_id input fields following the current
    target (updated on target change, so the fields stay hand-editable)."""
    global _ui_healer_id_input, _ui_loot_model_id_input
    try:
        tid = Player.GetTargetID()
    except Exception:
        tid = 0
    if tid == _target_track["id"]:
        return
    _target_track["id"] = tid
    mid = _current_target_model_id()
    _target_track["model_id"] = mid
    if mid > 0:
        _ui_healer_id_input = mid
        _ui_loot_model_id_input = mid


def _resolve(name: str) -> int | None:
    return map_names.resolve_map_id_by_name(
        name, prefer_ids=_active_portal_graph().known_map_ids()
    )


def _route_preview(start_name: str, target_name: str, prevent_outposts: bool):
    """Cached BFS route preview for the Main tab (recomputes on input change)."""
    start, target = _resolve(start_name), _resolve(target_name)
    if start is None or target is None:
        return None
    key = (start, target, prevent_outposts, settings.use_gwtoolbox_routing)
    if _route_cache["key"] != key:
        _route_cache["key"] = key
        _route_cache["route"] = routing.shortest_route(
            _active_portal_graph(), map_names, start, target, prevent_outposts
        )
    return _route_cache["route"]


def _checkbox(label: str, key: str):
    """Draw a checkbox bound to a settings attribute; mark dirty on change."""
    old = getattr(settings, key)
    new = PyImGui.checkbox(label, old)
    if new != old:
        setattr(settings, key, new)
        settings.mark_dirty()


def _text_input(label: str, key: str, buf: int = 128):
    old = getattr(settings, key)
    new = PyImGui.input_text(label, old, buf)
    if new != old:
        setattr(settings, key, new)
        settings.mark_dirty()


def _map_name_feedback(name: str):
    """Show how a typed map name resolves (green id or red unknown)."""
    if not name.strip():
        return
    mid = map_names.resolve_map_id_by_name(name, prefer_ids=_active_portal_graph().known_map_ids())
    if mid is None:
        PyImGui.text_colored("  -> unknown map name", (1.0, 0.35, 0.35, 1.0))
    else:
        kind = "outpost" if map_names.is_outpost(mid) else "explorable"
        in_graph = mid in _active_portal_graph().adjacency()
        note = "" if in_graph else ", NOT in portal graph"
        PyImGui.text_colored(
            f"  -> {map_names.MAP_ID_TO_NAME[mid]} ({mid}, {kind}{note})",
            (0.5, 1.0, 0.5, 1.0) if in_graph else (1.0, 0.8, 0.3, 1.0),
        )


