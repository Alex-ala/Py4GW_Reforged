class _HuffmanData:
    __slots__ = ("huffman_table", "helper_array", "temp_array", "var2")

    def __init__(self):
        self.huffman_table = [0] * 0x200
        self.helper_array = [0] * 0x48
        self.temp_array: Optional[list] = None
        self.var2 = 0


class GwDatDecompressError(Exception):
    pass


class _Decompressor:
    """Mirrors the `Decompress` class in xentax.cpp as closely as possible."""

    def __init__(self, words: tuple):
        self.words = words
        self.ptr = 0
        self.end = len(words)
        self.esi_8 = 0
        self.esi_c = 0
        self.esi_10 = 0

    def _consume_bits(self, temp: int) -> None:
        """Shared "consume `temp` bits, refilling from the input stream if
        the lookahead buffer runs low" logic -- this exact sequence appears
        4 times verbatim in the original decompiled source."""
        if temp:
            self.esi_c = _u32((self.esi_10 >> (0x20 - temp)) | (self.esi_c << temp))
        if temp > self.esi_8:
            if self.ptr == self.end:
                self.esi_8 = 0
                self.esi_10 = 0
            else:
                w = self.words[self.ptr]
                self.ptr += 1
                self.esi_10 = w
                ebp_8 = _u32(self.esi_8 - temp + 0x20)
                self.esi_c = _u32(self.esi_c | (self.esi_10 >> ebp_8))
                self.esi_10 = _u32(self.esi_10 << (temp - self.esi_8))
                self.esi_8 = ebp_8
        else:
            self.esi_8 = _u32(self.esi_8 - temp)
            self.esi_10 = _u32(self.esi_10 << temp)

    def _decode_symbol(self, hd: _HuffmanData) -> tuple[int, int]:
        """Decode one Huffman symbol from `hd`. Returns (temp, data) where
        `temp` is the code length just consumed and `data` the symbol."""
        i = (self.esi_c >> 0x18) * 2
        temp = hd.huffman_table[i]
        data = hd.huffman_table[i + 1]

        if temp == _M32:
            i = 0
            while self.esi_c < hd.helper_array[i]:
                i += 3
            temp = hd.helper_array[i + 2]
            data = _u32(hd.helper_array[i + 1] - (_u32(self.esi_c - hd.helper_array[i]) >> (0x20 - temp)))
            if data >= hd.var2:
                raise GwDatDecompressError("symbol index out of range")
            if hd.temp_array is None:
                raise GwDatDecompressError("missing temp_array")
            data = hd.temp_array[data]

        if temp >= 0x20:
            raise GwDatDecompressError("bad code length")

        self._consume_bits(temp)
        return temp, data

    def decompress(self, max_output: Optional[int] = None) -> bytes:
        words = self.words
        if len(words) < 4:
            raise GwDatDecompressError("input too short")

        self.esi_8 = 0
        self.esi_10 = 0

        self.esi_c = _u32(words[0] << 4)
        self.ptr = 1

        self.esi_c = _u32(self.esi_c | (words[1] >> 0x1c))
        self.esi_8 = 0x1c
        self.esi_10 = _u32(words[1] << 4)
        self.ptr = 2

        outsize = words[-1]
        output = bytearray(outsize)
        out_pos = 0

        ebp_18 = self.esi_c >> 0x1c
        self.esi_c = _u32((self.esi_10 >> 0x1c) | (self.esi_c << 4))

        self.esi_10 = _u32(self.esi_10 << 4)
        self.esi_8 = _u32(self.esi_8 - 4)

        if outsize == 0:
            return bytes(output)

        while out_pos != outsize:
            h1 = _HuffmanData()
            self._setup_nodes_and_tree(h1)
            h2 = _HuffmanData()
            self._setup_nodes_and_tree(h2)

            counter1 = self.esi_c >> 0x1c
            self.esi_c = _u32((self.esi_10 >> 0x1c) | (self.esi_c << 4))

            if self.esi_8 < 4:
                if self.ptr == self.end:
                    self.esi_8 = 0
                    self.esi_10 = 0
                else:
                    w = words[self.ptr]
                    self.esi_10 = _u32(w << (4 - self.esi_8))
                    self.esi_8 = _u32(self.esi_8 + 0x1c)
                    self.esi_c = _u32(self.esi_c | (w >> self.esi_8))
                    self.ptr += 1
            else:
                self.esi_10 = _u32(self.esi_10 * 16)
                self.esi_8 = _u32(self.esi_8 - 4)

            counter1 = _u32(((counter1 + 1) << 0xC) - 1)

            while counter1 >= 0:
                if out_pos == outsize:
                    break
                if max_output is not None and out_pos >= max_output:
                    return bytes(output[:out_pos])

                temp, data = self._decode_symbol(h1)

                if data < 0x100:
                    output[out_pos] = data
                    out_pos += 1
                else:
                    temp = TABLE4[data]
                    data = TABLE3[data]

                    if temp:
                        data |= self.esi_c >> (0x20 - temp)
                        if temp >= 0x20:
                            raise GwDatDecompressError("bad extra-bits length (len)")
                        self._consume_bits(temp)

                    match_len = _u32(ebp_18 + data + 1)

                    temp2, data2 = self._decode_symbol(h2)

                    dist_extra = TABLE5[data2]
                    backtrack = TABLE6[data2]

                    if dist_extra:
                        backtrack = backtrack | (self.esi_c >> (0x20 - dist_extra))
                        if dist_extra >= 0x20:
                            raise GwDatDecompressError("bad extra-bits length (dist)")
                        self._consume_bits(dist_extra)

                    if out_pos + match_len > outsize or backtrack >= out_pos:
                        # matches the original's defensive bail-out -- return
                        # whatever's been decoded so far rather than crash.
                        return bytes(output[:out_pos])

                    for _ in range(match_len):
                        output[out_pos] = output[out_pos - backtrack - 1]
                        out_pos += 1

                counter1 -= 1

        return bytes(output)

    def _setup_nodes_and_tree(self, hd: _HuffmanData) -> bool:
        ebp_128 = [0] * 32 + [_M32] * 32  # counts[0..31] + link-list heads[32..63] (-1 = empty)

        ebp_14 = self.esi_c >> 0x10
        self.esi_c = _u32((self.esi_10 >> 0x10) | (self.esi_c << 0x10))

        if self.esi_8 < 0x10:
            if self.ptr == self.end:
                self.esi_8 = 0
                self.esi_10 = 0
            else:
                self.esi_10 = self.words[self.ptr]
                self.ptr += 1
                self.esi_c = _u32(self.esi_c | (self.esi_10 >> (self.esi_8 + 0x10)))
                self.esi_10 = _u32(self.esi_10 << (0x10 - self.esi_8))
                self.esi_8 = _u32(self.esi_8 + 0x10)
        else:
            self.esi_8 = _u32(self.esi_8 - 0x10)
            self.esi_10 = _u32(self.esi_10 << 0x10)

        ebp_20 = [0] * ebp_14 if ebp_14 else []
        ebp_1c = 0

        ebp_8 = ebp_10 = _u32(ebp_14 - 1)

        if ebp_10 != _M32:  # i.e. ebp_14 - 1 != -1  <=>  ebp_14 != 0
            while True:
                i = 0
                while self.esi_c < TABLE1[i]:
                    i += 2
                j = ((i * 4 + 0x18) >> 3)
                ebp_4 = 0x20 - j
                arg_0 = TABLE2[TABLE1[i + 1] - (_u32(self.esi_c - TABLE1[i]) >> ebp_4)]
                if j >= 0x20:
                    raise GwDatDecompressError("bad tree-length code")
                if j:
                    self.esi_c = _u32((self.esi_10 >> ebp_4) | (self.esi_c << j))
                if j > self.esi_8:
                    if self.ptr == self.end:
                        self.esi_8 = 0
                        self.esi_10 = 0
                    else:
                        self.esi_10 = self.words[self.ptr]
                        self.ptr += 1
                        ebp_4 = _u32(self.esi_8 - j + 0x20)
                        self.esi_c = _u32(self.esi_c | (self.esi_10 >> ebp_4))
                        self.esi_10 = _u32(self.esi_10 << (j - self.esi_8))
                        self.esi_8 = ebp_4
                else:
                    self.esi_10 = _u32(self.esi_10 << j)
                    self.esi_8 = _u32(self.esi_8 - j)

                j = arg_0 >> 5
                arg_0 &= 0x1F

                if j > ebp_8:
                    return True  # matches the original's early "return true"

                if arg_0 or ebp_14 < 2:
                    ebp_128[arg_0] += j + 1
                    ebp_1c += j + 1
                    link = ebp_128[0x20 + arg_0]
                    cursor = ebp_8
                    remaining = j
                    while True:
                        if cursor >= ebp_14:
                            raise GwDatDecompressError("tree link overflow")
                        ebp_20[cursor] = link
                        link = cursor
                        cursor = _u32(cursor - 1)
                        remaining -= 1
                        if not (remaining + 1 and cursor != _M32):
                            break
                    ebp_128[0x20 + arg_0] = link
                    ebp_8 = cursor
                else:
                    ebp_8 = _u32(ebp_8 - (j + 1))

                if ebp_8 == _M32:
                    break

        if ebp_14 and not ebp_1c:
            ebp_20[ebp_10] = ebp_128[0x20]
            ebp_128[0x20] = ebp_10
            ebp_128[0] = 1
            ebp_1c = 1

        hd.huffman_table = [0] * 0x200

        arg_0 = 0
        ebp_24 = 0
        ebp_8 = 0
        ebp_4 = 8
        ebp_18_idx = 0x20  # EBPminus18 = EBPminus128 + 0x20

        while arg_0 <= 8:
            v = ebp_128[ebp_18_idx]
            if v != _M32:
                ebp_10 = 1 << arg_0
                while True:
                    if ebp_8 >= ebp_10 or v >= ebp_14:
                        return True
                    i = (1 << ebp_4) - 1
                    if 1 << ebp_4:
                        ebp_28 = ebp_8 << (8 - arg_0)
                        while True:
                            if (ebp_28 | i) >= 0x100:
                                raise GwDatDecompressError("bad prefix fill")
                            hd.huffman_table[(ebp_28 | i) * 2] = arg_0
                            hd.huffman_table[(ebp_28 | i) * 2 + 1] = v
                            i -= 1
                            if not (i + 1):
                                break
                    v = ebp_20[v]
                    ebp_24 += 1
                    ebp_8 -= 1
                    if v == _M32:
                        break

            ebp_18_idx += 1
            ebp_4 -= 1
            ebp_8 = _u32(2 * ebp_8 + 1)
            arg_0 += 1

        if ebp_24 > ebp_1c:
            raise GwDatDecompressError("huffman tree overflow")

        if ebp_24 != ebp_1c:
            ebp_10 = ebp_1c - ebp_24
            if hd.var2 > ebp_10:
                hd.var2 = ebp_10
            hd.temp_array = [0] * ebp_10
            if hd.var2 < ebp_10:
                hd.var2 = ebp_10

            v = 0
            if arg_0 <= 0x1F:
                helper_idx = 1  # EBPminus18 = HData.HelperArray + 1
                while arg_0 <= 0x1F:
                    ptr_val = ebp_128[0x20 + arg_0]
                    if ptr_val != _M32:
                        e = ptr_val
                        ebp_10 = 1 << arg_0
                        while True:
                            if ebp_8 > ebp_10 or e > ebp_14:
                                return True
                            hd.huffman_table[(ebp_8 >> (arg_0 - 8)) * 2] = _M32
                            if v >= hd.var2:
                                raise GwDatDecompressError("temp_array overflow")
                            hd.temp_array[v] = e
                            e = ebp_20[e]
                            v += 1
                            ebp_8 -= 1
                            if e == _M32:
                                break
                        helper_idx += 3
                        hd.helper_array[helper_idx - 4] = _u32((ebp_8 + 1) << (0x20 - arg_0))
                        hd.helper_array[helper_idx - 3] = v - 1
                        hd.helper_array[helper_idx - 2] = arg_0
                    arg_0 += 1
                    ebp_8 = ebp_8 + ebp_8 + 1
                return True
        return True


def decompress(data: bytes, max_output: Optional[int] = None) -> bytes:
    """Decompress one GWDat-compressed entry's raw bytes.

    `max_output`, if given, stops as soon as at least that many output bytes
    are produced and returns the (possibly larger, block-granular) partial
    result -- for cheaply probing a candidate's structure without paying for
    a full decompress of files that turn out not to match.
    """
    n_words = len(data) // 4
    words = struct.unpack(f'<{n_words}I', data[: n_words * 4])
    return _Decompressor(words).decompress(max_output)


# ─── MFT / archive parsing (ported from GWUnpacker.cpp) ────────────────────

@dataclass(slots=True)
class MFTEntry:
    offset: int
    size: int
    a: int   # compression type: 0 = uncompressed, nonzero (8) = compressed
    b: int   # content flag: 0 = empty/unreadable
    c: int
    id: int
    crc: int
    hash: Optional[int] = None


class GwDatArchive:
    def __init__(self, path: str):
        self._f = open(path, 'rb')
        self._read_header()
        self._read_mft()

    def close(self) -> None:
        self._f.close()

    def __enter__(self) -> "GwDatArchive":
        return self

    def __exit__(self, *exc) -> None:
        self.close()

    def _read_header(self) -> None:
        data = self._f.read(32)
        magic, header_size, sector_size, crc1, mft_offset, mft_size, flags = struct.unpack('<4siiiqii', data)
        if magic != bytes([0x33, 0x41, 0x4E, 0x1A]):
            raise ValueError(f"Not a Gw.dat file (bad magic {magic!r})")
        self.mft_offset = mft_offset
        self.mft_size = mft_size

    def _read_mft(self) -> None:
        f = self._f
        f.seek(self.mft_offset)
        _mfth_id, _u1, _u2, entry_count, _u4, _u5 = struct.unpack('<4siiiii', f.read(24))

        entries: list[MFTEntry] = []
        for _ in range(15):
            entries.append(_read_mft_entry(f))

        hashlist_entry = entries[1]
        f.seek(hashlist_entry.offset)
        n_hash = hashlist_entry.size // 8
        hashlist = [struct.unpack('<ii', f.read(8)) for _ in range(n_hash)]
        hashlist.sort(key=lambda t: t[1])

        hashcounter = 0
        while hashlist[hashcounter][1] < 16:
            hashcounter += 1

        hash_to_entry: dict[int, MFTEntry] = {}
        f.seek(self.mft_offset + 24 * 16)
        for x in range(16, entry_count - 1):
            me = _read_mft_entry(f)
            entries.append(me)

            if hashcounter < len(hashlist) and x == hashlist[hashcounter][1]:
                file_number, _file_offset = hashlist[hashcounter]
                me.hash = file_number
                hash_to_entry[file_number] = me

                while hashcounter + 1 < len(hashlist) and hashlist[hashcounter][1] == hashlist[hashcounter + 1][1]:
                    hashcounter += 1
                    file_number, _file_offset = hashlist[hashcounter]
                    hash_to_entry[file_number] = me

                hashcounter += 1

        self.entries = entries
        self.hash_to_entry = hash_to_entry

    def read_by_hash(self, file_id: int) -> Optional[bytes]:
        entry = self.hash_to_entry.get(file_id)
        if entry is None or not entry.b:
            return None
        self._f.seek(entry.offset)
        raw = self._f.read(entry.size)
        if entry.a:
            return decompress(raw)
        return raw


def _read_mft_entry(f) -> MFTEntry:
    offset, size, a, b, c, id_, crc = struct.unpack('<qiHBBii', f.read(24))
    return MFTEntry(offset=offset, size=size, a=a, b=b, c=c, id=id_, crc=crc)
