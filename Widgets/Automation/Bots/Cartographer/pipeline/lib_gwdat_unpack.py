"""
Offline Gw.dat archive reader + decompressor.

MFT layout ported from GWUnpacker.cpp (Jonathan-Greve/GuildWarsMapBrowser).
Decompression algorithm ported from xentax.cpp in the same project (a
bit-level Huffman+LZ scheme; the community calls it "GWDat compression").
Both are decompiler-derived reconstructions of the shipped game code, not
something re-derived here -- this module is a mechanical line-for-line
Python transcription, kept close to the original variable names so it can
be diffed against the C++ source if something doesn't decode right.
"""
